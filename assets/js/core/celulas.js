export function parseMaquina(label) {
  const [tnl, patrimonio] = String(label || "").split("-");
  return {
    id: String(label || ""),
    tnl: String(tnl || "").padStart(3, "0"),
    patrimonio: String(patrimonio || "")
  };
}

export function normalizeCelula(value) {
  const raw = String(value || "").trim().toUpperCase();
  const digits = raw.replace(/\D/g, "");
  if (digits) return `CÉLULA ${digits.padStart(2, "0")}`;
  if (CELULAS[raw]) return raw;
  return "CÉLULA 05";
}

export function getCelulaFromUser(user) {
  return normalizeCelula(user?.celula_padrao || user?.linha_padrao || "CÉLULA 05");
}
